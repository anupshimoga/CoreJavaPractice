//WAP to print 1-100 without using numbers
package com.corejava.trickyquestions;

public class PrintWithoutUsingNumbers {
public static void main(String[] args) {
	// ASCII value of d is 100 
	for(int i = 'd'/'d'; i<='d'; i++) {
		System.out.println(i);
	// String length of 10 multiplied by 10 and print all the values	
	String s = "..........";
	for(int i1= 'd'/'d'; i1<=(s.length()*s.length()); i1++) {
		System.out.println(i1);
	}
	}
}
}
